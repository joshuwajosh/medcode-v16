    echo ""
    echo "=========================================="
    echo "  MedCode AI V16 — Deployment Complete"
    echo "=========================================="
    echo ""
    echo "Access Points:"
    echo "  - API: http://localhost:8000/docs"
    echo "  - Dashboard: http://localhost:8000/dashboard/"
    echo "  - Health: http://localhost/health"
