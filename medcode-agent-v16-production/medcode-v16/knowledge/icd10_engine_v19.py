def lookup_code(code: str) -> Optional[Dict]:
    """Look up an ICD-10 code across all chapters."""
    for chapter_range, chapter_data in ICD10_CHAPTERS.items():
        if code in chapter_data["codes"]:
            return {
                "code": code,
                "description": chapter_data["codes"][code],
                "chapter": chapter_data["name"],
                "chapter_range": chapter_range,
            }
    # Check parent codes
    parent = code[:-1] if len(code) > 3 else code
    if parent != code:
        result = lookup_code(parent)
        if result:
            return result
    return None


def search_codes(query: str, limit: int = 20) -> List[Dict]:
    """Search ICD-10 codes by description."""
    results = []
    query_lower = query.lower()
    for chapter_range, chapter_data in ICD10_CHAPTERS.items():
        for code, description in chapter_data["codes"].items():
            if query_lower in description.lower():
                results.append({
                    "code": code,
                    "description": description,
                    "chapter": chapter_data["name"],
                })
                if len(results) >= limit:
                    return results
    return results


def get_seventh_character(code: str, encounter_type: str = "initial") -> str:
    """Get the 7th character extension for injury/external cause codes."""
    ext_map = {"initial": "A", "subsequent": "D", "sequela": "S"}
    return ext_map.get(encounter_type, "A")


def get_diabetes_type(note_text: str) -> str:
    """Determine diabetes type from clinical note."""
    text_lower = note_text.lower()
    if "type 1" in text_lower or "t1dm" in text_lower or "juvenile" in text_lower:
        return "E11"
    if "type 2" in text_lower or "t2dm" in text_lower or "adult onset" in text_lower:
        return "E11"
    return "E11"
